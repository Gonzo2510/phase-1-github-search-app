function createCard(data){
    const userList = document.getElementById('user-list')
    const a = document.createElement('a')
    const h3 = document.createElement('h3')
    const img = document.createElement('img')

    img.src = data.items[0].avatar_url
    img.width = 50
    img.height = 50

    a.href = data.items[0].html_url
    a.innerHTML = " Website"

    h3.id = data.items[0].login
    h3.innerHTML = data.items[0].login

    h3.appendChild(img)
    h3.appendChild(a)
    userList.appendChild(h3)
}

function createRepos(repos) {
    const reposList = document.getElementById('repos-list')
    repos.forEach(repo => {
        let a = document.createElement('a')
        let br = document.createElement('br')
        a.innerHTML = `${repo.full_name} \n`
        a.href =  repo.html_url
        reposList.appendChild(a)
        reposList.appendChild(br)
    });
}

function findUserByName(name) {
    fetch(`https://api.github.com/search/users?q=${name}`, {
        method: 'GET',
        headers: {
            'Accept': 'application/vnd.github.v3+json'
        }
    })
    .then(response => response.json())
    .then(data => {
        createCard(data)
        findRepos(name)
    })
    .catch(error => {
        //catch error
    })
}

function findRepos(name) {
    fetch(`https://api.github.com/users/${name}/repos`, {
        method: 'GET',
        headers: {
            'Accept': 'application/vnd.github.v3+json'
        }
    })
    .then(response => response.json())
    .then(repos => {
        //console.log(repos)
        createRepos(repos)
    })
    .catch(error => {
        //catch error
    })
}

function submitForm() {
    let form = document.getElementById('github-form')
    form.addEventListener('submit', (e) => {
        e.preventDefault()
        let name = e.target[0].value
        findUserByName(name)
    })
}


